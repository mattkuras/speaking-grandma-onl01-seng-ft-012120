# Write a speak_to_grandma method.
def speak_to_grandma(string) 
  love = "I LOVE YOU GRANDMA"
  if string !== string.capitals 
    print "HUH?! SPEAK UP, SONNY!"
  elsif string == love 
    print "I LOVE YOU TOO PUMPKIN"
  else print "NO, NOT SINCE 1938!"  
end
